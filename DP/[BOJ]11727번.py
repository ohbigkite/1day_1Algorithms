# 2xn크기의 직사각형을 1x2, 2x2 ,2x1 타일로 채우는 방법의 수 구하기
# d[n] = d[n-1]+ 2 * d[n-2] 
# d[3] = 2+1 = 4
# d[4] = 2+2 = 4
#     = 2+1+1 = 3* 2 = 6
#     = 1+!+1+!          -> 11
# d[5] = 2+2+1 = 3*2*2 = 12
#     = 2+1+1+1 = 4 *2 = 8
#     = 1-> 21             5
# d[6] = 2+2+2 = 2*2*2 = 8
#     = 2+2 +1 + 1 = 6 * 2* 2 = 24
#       = 2+1+1+1+1 = 2*5 = 10
#       = 1+1+1+1+1+1 =1  -> 43


n = int(input())

d = [0] * (n+1)


if n == 1:
    print(1)

else:
    d[1] = 1
    d[2] = 3
    for i in range(3, n+1):
        d[i] = d[i-1] + 2 * d[i-2]
    
    print(d[n] % 10007)




